// Flatten for quick lookup
export function findBirthplace(province: string, cityName: string): BirthplaceInfo | undefined {
  const group = BIRTHPLACE_DATABASE.find(g => g.province === province)
  return group?.cities.find(c => c.name === cityName)
}

export function getAllProvinces(): string[] {
  return BIRTHPLACE_DATABASE.map(g => g.province)
}

export function getCitiesByProvince(province: string): BirthplaceInfo[] {
  return BIRTHPLACE_DATABASE.find(g => g.province === province)?.cities || []
}
